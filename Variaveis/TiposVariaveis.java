package Variaveis;

public class TiposVariaveis {

    public static void main(String[] args) {
        //variaveis
        byte idade = 123;
        short ano = 2021;
        int cep = 21070333; // Se começar com zero, talves tenha que ser outro tipo.
        long cpf = 98765432109l; // Se começar com zero, talves tenha que ser outro tipo.
        float pi = 3.14f; // Esse tipo em que por um F no final, pode ser minusculo
        double salario = 1247.55;
        //Tipo long tambem precisa terminar com l

        //Representação de texto
        String meuNome = "Luiz Guni";
        System.out.println(meuNome);
    }

}
